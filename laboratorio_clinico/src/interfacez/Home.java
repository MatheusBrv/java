    private void tabla_citasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_citasMouseClicked
        int columna = tabla_citas.getSelectedRow();
        if (evt.getClickCount() == 2) {

            add_cites addc = new add_cites();
            addc.setVisible(true);

            add_cites.txt_turn.setText(tabla_citas.getModel().getValueAt(columna, 0).toString());
            add_cites.txt_client.setText(tabla_citas.getModel().getValueAt(columna, 1).toString());

//---------------------------------------------------------------------------
            ////importamos librerias para darle formato a nuestra fecha
            SimpleDateFormat formatoDelTexto = new SimpleDateFormat("yy-MM-dd");
            String fecha = tabla_citas.getModel().getValueAt(columna, 2).toString();
            Date dato = null;//date es un libreria
            try {
                dato = formatoDelTexto.parse(fecha);
            } catch (ParseException ex) {
                System.out.println(ex);
            }
            add_cites.jc_date.setDate(dato);
//-------------------------------------------------------------------------------------------
            add_cites.combo_hora.setSelectedItem(tabla_citas.getModel().getValueAt(columna, 3).toString());
            add_cites.combo_tipos.setSelectedItem(tabla_citas.getModel().getValueAt(columna, 4).toString());
            add_cites.combo_examen.setSelectedItem(tabla_citas.getModel().getValueAt(columna, 5).toString());
            add_cites.txt_price.setText(tabla_citas.getModel().getValueAt(columna, 6).toString());

            add_cites.btn_save.setEnabled(false);
            add_cites.btn_update.setEnabled(true);
        }
    }//GEN-LAST:event_tabla_citasMouseClicked

